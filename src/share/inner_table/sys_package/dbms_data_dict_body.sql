CREATE OR REPLACE PACKAGE BODY dbms_data_dict AS
  PROCEDURE TRIGGER_DUMP(DICT_ITEM_RETENTION_BY_DAY BINARY_INTEGER DEFAULT NULL);
    PRAGMA INTERFACE(C, DBMS_DATA_DICT_TRIGGER_DUMP);

  PROCEDURE ENABLE_DUMP(UNUSED_ARG BINARY_INTEGER DEFAULT NULL);
    PRAGMA INTERFACE(C, DBMS_DATA_DICT_ENABLE);

  PROCEDURE DISABLE_DUMP(UNUSED_ARG BINARY_INTEGER DEFAULT NULL);
    PRAGMA INTERFACE(C, DBMS_DATA_DICT_DISABLE);

  PROCEDURE MODIFY_DUMP_INTERVAL(REPEAT_INTERVAL_EXPR IN VARCHAR2 DEFAULT NULL);
    PRAGMA INTERFACE(C, DBMS_DATA_DICT_MODIFY_DUMP_INTERVAL);

  PROCEDURE MODIFY_RUN_DURATION(MAX_RUN_DURATION_BY_SEC BINARY_INTEGER DEFAULT NULL);
    PRAGMA INTERFACE(C, DBMS_DATA_DICT_MODIFY_RUN_DURATION);

  PROCEDURE MODIFY_DICT_ITEM_RETENTION(DICT_ITEM_RETENTION_BY_DAY BINARY_INTEGER DEFAULT NULL);
    PRAGMA INTERFACE(C, DBMS_DATA_DICT_MODIFY_RETENTION);

END dbms_data_dict;
